"""In Python, we create a string by putting quotes around text because
programming languages need to distinguish between different types of
values.
To use more than one line of text in your string (called a multiline string),
use three single quotes (’’’). """

fred = "Why do gorillas have big nostrils? Big fingers!!"
print(fred)
fred = 'What is pink and fluffy? Pink fluff!!'
print(fred)
fred = '''How do dinosaurs pay their bills?
With tyrannosaurus checks!'''
print(fred)
