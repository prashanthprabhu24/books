"""
We can join lists by adding them, just like adding numbers, using a plus (+)
sign.
"""

list1 = [1, 2, 3, 4]
list2 = ['I', 'tripped', 'over', 'and', 'hit', 'the', 'floor']
print(list1 + list2)

# We can also add the two lists and set the result to another variable
list1 = [1, 2, 3, 4]
list2 = ['I', 'ate', 'chocolate', 'and', 'I', 'want', 'more']
list3 = list1 + list2
print(list3)

# And we can multiply a list by a number. For example, to multiply list1 by 5,
list1 = [1, 2]
print(list1 * 5)

